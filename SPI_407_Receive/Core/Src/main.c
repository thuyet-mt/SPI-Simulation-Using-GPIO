uint8_t SPI_transfer(uint8_t byte_out){
	uint8_t byte_in = 0;
	uint8_t ibit, res;

	while(read_SS == HIGH);

	for(ibit = 0x80;ibit>0;ibit = ibit>>1){
		res = (byte_out & ibit );
		if(res){
			MISO_HIGH;
		}
		else {
			MISO_LOW;
		}

		while(read_SCK == LOW);
		if (read_MOSI == HIGH ){
			byte_in = byte_in | ibit;
		}
		while(read_SCK == HIGH);
	}
	return byte_in;
}
